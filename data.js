var requests = {};

$(document).ready(function() {
    var viewport_height = $(window).height() - $("nav").outerHeight(true);
    
    var main = $("#main").height(viewport_height);
    var chooser = $("#chooser").height(viewport_height);
    var preference = $("#preference").height(viewport_height);
    var cuisines = $("#cuisines").height(viewport_height);
    var result = $("#result").height(viewport_height);
    
    requests.cuisine = [];
    
    main.show();
    $("#hungrybutton").click(function() {
        main.hide();
        chooser.show();
    });
    $(".price").click(function() {
        var price;
        switch ($(this).html()) {
            case "$":
                price = 1;
                break;
            case "$$":
                price = 2;
                break;
            case "$$$":
                price = 3;
                break;
            default:
                console.error("Something broke...");
                break;
        }
        requests.price = price;
        chooser.hide();
        preference.show();
    });
    $(".meal").click(function() {
        var meal;
        switch ($(this).html()) {
            case "Breakfast":
                meal = "breakfast";
                break;
            case "Lunch":
                meal = "lunch";
                break;
            case "Dinner":
                meal = "dinner"
                break;
            case "Desert":
                meal = "desert";
                break;
            default:
                console.log("Something went horribly wrong...");
        }
        requests.meal = meal;
        preference.hide();
        cuisines.show();
    });
    
    $('.cuisines input[cuisine="checkbox"]').on("click", function() {
        requests.cusine.push($(this).html());
    });
    
    $('#cuisines .submit').click(function() {
        $('#result').text(get_resturaunt().name);
        cuisines.hide();
        result.show();
        
    });
});

function get_resturaunt() {
	var hits = [];
	
	for(var i in restaurants){
		var current_restaurant = restaurants[i];
		if(current_restaurant.cuisine.includes(requests.cuisine) &&
		current_restaurant.meals.includes(requests.meal) &&
		current_restaurant.price == requests.price) {
			hits.push(current_restaurant);
		}
	}
	return hits[Math.floor(Math.random()*hits.length)];
}
